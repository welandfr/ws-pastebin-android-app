package fi.fw_teaching.pastebin;

public class PastebinObj {
    String val;
    int status;

    public PastebinObj() {}

    public String getVal() {
        return val;
    }

    public void setVal(String val) {
        this.val = val;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }
}
